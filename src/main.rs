fn main() {
    // addition
    let _sum = 5 + 10;
    println!("{}", _sum);

    // subtraction
    let _subtraction = 95.5 - 4.3;
    println!("{}", _subtraction);

    // multiplication
    let _multiplication = 4 * 30;
    println!("{}", _multiplication);

    // division
    let _quotient = 56.7 / 32.2;
    let _truncated = -5 / 3; // Results in -1
    println!("{}", _quotient);
    println!("{}", _truncated);

    // remainder
    let _remainder = 43 % 5;
    println!("{}", _remainder);
}
